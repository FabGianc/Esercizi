""" Programma che legge un intero e stampa un messaggio che indica se l'intero è pari o dispari """

numero = int(input("Caro utente introduci un intero: ")) # per memorizzare l'intero 

# Controlla se il numero è pari
if(numero%2==0):
	print("Il numero e' pari!")
else:
	print("Il numero e' dispari!")	

